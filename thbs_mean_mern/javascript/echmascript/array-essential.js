//push method
let msg =['Happy' , 'Ugadhi'];
msg.push('good' , 'to' , 'see' , 'you'); //added items end of the array
 console.log (`${msg}`); 

 // pop method 
msg.pop(); //removed items end of the array
console.log(`${msg}`); 

//unshift method

msg.unshift('Hey','Buddy'); //added items starting of the array
console.log(`${msg}`);

//shift method 

msg.shift();    //removed items starting of the array
console.log(`${msg}`);
